def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def soma(a, b):
    soma = a + b
    return soma

def sub(a, b):
    sub = a - b
    return sub

def mult(a, b):
    mult = a * b
    return mult

def div(a, b):
    div = a / b
    return div